// Operadores relacionais

// Operadores mais comuns:

/*
    Igual a ==
    Igual e do mesmo tipo que ===
    Diferente de !=
    Diferente ou de outro tipo que !==
    Maior que >
    Menor que <
    Maior ou igual a >=
    Menor ou igual a <=
*/

var n1 = 54, n2 = 78, n3 = 100, n4 = "100";

console.log(n1 == n2);
console.log(n1 === n2);
console.log(n1 != n2);
console.log(n1 !== n2);
console.log(n1 > n2);
console.log(n1 < n2);
console.log(n1 >= n2);
console.log(n1 <= n2);

console.log(n3 !== n4);