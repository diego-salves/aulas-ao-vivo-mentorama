var array = ['diego', 'damião', 'diogo']

//for
for(var i = 0; i < array.length; i++){
    console.log(`No índice ${i} temos o(a) ${array[i]}`)
}

//while
var numero = 20;

while(numero <= 10){
    console.log(numero);
    numero++;
};

//do-while
var contador = 10;

do{
    console.log(`Contador: ${contador}`);
    contador++;
}while(contador <= 5);

